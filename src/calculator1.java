     public double det(){double det=0.0;
         
         r1.setEnabled(false);
         r2.setEnabled(false);
         r3.setEnabled(false);
         r4.setEnabled(false);
         row=rowfun();
         col=colfun();
         int m=0;
         double a[]=new double[4];
        
        
         if(row==2&&col==2){ 
             double a1=Double.parseDouble(t11.getText());
             double a2=Double.parseDouble(t12.getText());
             double a3=Double.parseDouble(t21.getText());
             double a4=Double.parseDouble(t22.getText());
             det=(a4*a1)-(a2*a3);}
         else   if(row==3&&col==3){ 
             
             for(int i=0;i<3;i++){
                 for(int j=0;j<3;j++){
                     for(int k=0;k<3;k++){
                         if(j!=0&&k!=i)
                         {
                         if(m==0){
                             a[0]=Double.parseDouble(jText[j][k].getText());
                             m=1;
                             
                         } 
                     else if(m==1){
                             a[1]=Double.parseDouble(jText[j][k].getText());
                             m=2;
                             
                         } 
                          else if(m==2){
                             a[2]=Double.parseDouble(jText[j][k].getText());
                             m=3;
                             
                         } 
                          else if(m==3){
                             a[3]=Double.parseDouble(jText[j][k].getText());
                             m=0;
                             
                         } 
                         }            
                     }
                 }
              double b=Double.parseDouble(jText[0][i].getText());
              double c=b*(Math.pow(-1,i)*((a[0]*a[3])-(a[2]*a[1])));
              det=det+c;
              
              
             }
             
         }
         else{
             JOptionPane.showMessageDialog(null,"This operation can only be performed on square matrices");
             
         }
     

        return det;
     }
